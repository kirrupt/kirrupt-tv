package kirrupt.tvrecommendation.findSimilarities.utilities;

/**
 * A triple consisting of a pair of integers and a double.  
 *
 * @author Amrit Tuladhar
 */
public class IntDoubleIntTriple {
    public int a;
    public double b;
    public int c;
    
    public IntDoubleIntTriple(int a, double b, int c) {
	this.a = a;
	this.b = b;
	this.c = c;
    }
}
